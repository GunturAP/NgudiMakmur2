                     <center>
                         <h2 class="section-heading mb-0">
                             <span class="section-heading-upper"> Cara Budidaya Kedelai Agar Kualitas Maksimal</span>
                         </h2>
                     </center>
                     <br>
                     <ol>
                         <b>
                             <li>
                                 Pilih Varietas Kedelai Unggulan
                             </li>
                         </b>
                         <p>
                             Ada begitu banyak varietas kedelai yang bisa Anda pilih dipasaran. Namun, pastikan Anda memilih kedelai yang unggul.

                             Varietas kedelai unggulan umumnya memiliki beberapa sifat unggul, seperti produktivitas tinggi, umur pendek, tahan kecaman tanah masam, memiliki ketahanan terhadap serangan hama penyakit, seperti karat daum, hama Agromyza, hama Lamprosema litura, hama wereng kedelai, hama penggerek polong, dan lainnya.

                             Anda bisa menentukan varietas kedelai terbaik yang sesuai untuk ditanam di daerah Anda. Tentu saja, ini berdasarkan kondisi lingkungan di tempat Anda.
                         </p>

                         <b>
                             <li>
                                 Jenis tanah dan kondisi Iklim
                             </li>
                         </b>

                         <p>
                             Tanam kacang kedelai pada tanah yang cocok dan sesuai dengan kondisi tanaman kedelai.
                             Ciri-ciri tanah yang sesuai untuk kacang kedelai diantaranya adalah:
                         <ol>
                             <li>pH tanah antara 4,5-5,5</li>
                             <li>Bertekstur lempung, berpasir, ataupun liat berpasir.</li>
                             <li>Pilih lokasi dan tanah yang terbebas dari penyakit endemik.</li>
                             <li>Suhu tanah yang sesuai adalah sekitar 30°C.</li>
                             <li>Panjang hari (photoperiode) tanaman kedelai merupakan jenis tanaman hari pendek atau maksimal 15 jam per hari dan optimalnya 12 jam.</li>
                             <li>Kebutuhan air selama masa pertumbuhan kedelai rata-rata adalah antara 350 – 450 mm.</li>

                         </ol>
                         <br>
                         Setelah memilih jenis tanah yang sesuai,
                         selanjutnya Anda bisa mulai melakukan pengolahan tanah secara organik. Berikut ini adalah cara pengolahan tanah untuk tanaman kedelai:

                         </p>
                         <b>
                             <li>
                                 Pengolahan Lahan
                             </li>
                         </b>
                         <p>
                             Pengolahan tanah dapat menggunakan mesin ataupun dengan cara tradisional.
                             Alat yang digunakan untuk pengolahan tanah ini bergantung dengan kemudahan Anda dalam mengakses peralatan pertanian tersebut.
                             Apapun alat yang Anda gunakan, pastikan Anda mengolah tanah hingga kedalaman lebih dari 50 cm.
                             Dengan begitu, akar tanaman bisa tumbuh dan menembus kedalam tanah hingga 5 meter.
                             Semakin baik pertumbuhan akar tanaman, maka kemampuan tanaman dalam menyerap hara akan semakin baik. Dengan begitu, pertumbuhan dan pembuahan tanaman kedelai akan lebih optimal.
                         <ol>
                             <li>Bersihkan lahan dari jerami padi / tanaman yang masih tersisa dari pertanaman sebelumnya.</li>
                             <li>Balik tanah menggunakan traktor atau cagkuli tanah dengan kedalaman antara 40-50 cm.</li>
                             <li>Buat petak-petak penanaman kedelai (gundukan) dengan ukuran lebar 3m dan panjang yang disesuaikan dengan panjang lahan. Usahakan gundukan dibuat dengan arah timur-barat dan sejajar dengan drainase agar tidak terjadi genangan air.</li>
                             <li>Diantara petakan tersebut, buat saluran drainase untuk mengatur kondisi pengairan kedelai selebar 25 dan sedalam 30 cm.</li>
                             <li>Sirami tanah hingga basah.</li>
                             <li>Diamkan tanah selama 1 minggu agar proses penggemburan dan penyuburan tanah oleh bakteri bisa lebih optimal.</li>
                         </ol>
                         </p>

                         <b>
                             <li>
                                 Pemilihan Benih Kedelai
                             </li>
                         </b>
                         <p>
                             Pemilihan benih kedelai memegang peranan penting dalam proses penanaman kedelai. Pilihlah benih yang mampu beradaptasi dengan kondisi lapang dan memenuhi standar mutu benih.

                             Beberapa indikator benih yang baik adalah produktivitas tinggi, umur panen pendek, ukuran biji besar, warna biji cerah, dan memiliki tingkat adaptasi lingkungan yang tinggi. Jangan lupa juga pilih benih dari varietas unggul yang cocok didaerah Anda.

                             Untuk menjamin kualitas benih yang baik, gunakan benih dari pihak pembenihan tersertifikasi. Pastikan benih Anda masih dikemas rapat dan belum kadaluarsa.
                         </p>
                         <b>
                             <li>
                                 Cara Penanaman Kacang Kedelai
                             </li>
                         </b>
                         <p>
                             Cara tanam kacang kedelai dimulai dari pembuatan lubang tanam setelah media siap. Untuk lebih jelasnya, berikut ini adalah cara menanam kacang kedelai:
                         <ol>
                             <li>Buat lubang tanam dengan kedalam 2 cm.</li>
                             <li>Jarak tanam yang disarankan adalah 40 cm X 15 cm atau 400.000 tanaman/ha.</li>
                             <li>Isi lubang tanam tersebut dengan 3 biji kacang kedelai.</li>
                             <li>Tutup lubang tanam dengan tanah.</li>
                             <li>Sirami dengan air hingga tanah basah dan lembab.</li>
                         </ol>
                         <br>
                         Setelah ditanam, maka lanjutkan dengan melakukan perawatan dengan cara berikut:
                         </p>
                         <b>
                             <li>
                                 Perawatan Kacang Kedelai
                             </li>
                         </b>
                         <p>
                             Perawatan budidaya kedelai sebenarnya tidaklah sulit. Namun, Anda harus memahami hal-hal penting yang seharusnya dilakukan agar proses perawatan tanaman kedelai dapat optimal.
                             Untuk lebih jelasnya, berikut ini adalah cara merawat tanaman kedelai:

                         </p>
                         <ol type="a">

                             <b>
                                 <li>
                                     Pemasangan Mulsa
                                 </li>
                             </b>
                             <p>
                                 Untuk menekan pertumbuhan gulma lakukan pemasangan mulsa pada gundukan/bedengan.
                                 Pasang mulsa di seluruh permukaan bedengan, kecuali di sekitar lubang tanam. Lakukan pemasangan mulsa sejak sebelum penanaman benih.
                             </p>
                             <b>
                                 <li>
                                     Penyulaman
                                 </li>
                             </b>
                             <p>
                                 Penyulaman bertujuan untuk menanam kembali tanaman yang mati dalam satu lubang tanam. Segera lakukan penyulaman setelah benih mulai tumbuh.
                                 <br>
                                 Jika Anda terlambat melakukan penyulaman, maka berdampak pada tingkat pertumbuhannya yang menjadi jauh berbeda. Oleh karena itu, pada stadium awal pertumbuhan kedelai (15-20 HST),
                                 Anda disarankan untuk segera melakukan penyulaman agar tidak mempengaruhi produktifitas tanaman.
                             </p>
                             <b>
                                 <li>
                                     Pengairan
                                 </li>
                             </b>
                             <p>
                                 Pengairan tanaman kedelai memegang peranan penting dalam pertumbuhan tanaman.
                                 Jika tanaman kedelai tidak mendapatkan air yang cukup, maka tanaman akan rentan mengalami kematian.
                             <ul>
                                 <li>Utamanya, tanaman berada di masa sangat rentan ketika berada di fase perkecambahan (0-5 HST) dan stadium awal (15-20 HST).</li>
                                 <li>Setelah melewati fase tersebut, tanaman tetap membutuhkan air dalam jumlah cukup untuk proses pembentukan biji pada umur 35-65 HST.</li>
                                 <li>Lakukan pengairan saat pagi atau sore hari dengan cara di siram manual ataupun dengan menggunakan saluran drainase selama 15-30 menit. Ini bertujuan agar tanah tidak terlalu becek ataupun terlalu kering.</li>
                             </ul>

                             </p>
                             <b>
                                 <li>
                                     Penyiangan Gulma
                                 </li>
                             </b>
                             <p>
                                 Meski Anda sudah memasang mulsa, namun penyiangan gulma tetap harus dilakukan untuk memastikan lahan terbebas dari gulma.
                             <ul>
                                 <li>Jika Anda menggunakan mulsa, Anda bisa mulai melakukan penyiangan gulma saat tanaman kedelai berumur 20 HST.</li>
                                 <li>Namun, jika Anda tidak menggunakan mulsa, maka Anda bisa melakukan penyiangan gulma saat tanaman berumur 10 HST dan diulangi setiap 1 minggu sekali.</li>
                             </ul>
                             </p>

                             <b>
                                 <li>
                                     Panen dan Pasca Panen
                                 </li>
                             </b>
                             <p>
                             <ol>
                                 <b>
                                     <li>
                                         Panen
                                     </li>
                                 </b>
                                 <p>
                                     Panen dilakukan apabila semua daun tanaman telah rontok, polong berwarna kuning/coklat, dan telah mengering.
                                     Panen dilakukan dengan memotong pangkal batang pakai sabit. Hindari panen dengan cara mencabut tanaman untuk menghindari
                                     tercampurnya hasil panen dengan tanah.
                                     <br>
                                     Perontakan dapat dilakukan dengan menggunakan Power Treser (Perontok dengan menggunakan mesin)
                                     atau dengan cara manual pakai kayu. Perontokan dengan cara manual sebaiknya menggunakan kayu yang
                                     tidak bersegi untuk menghidari pecahnya biji akibat pukulan kayu.
                                 </p>

                                 <b>
                                     <li>
                                         Pasca Panen
                                     </li>
                                 </b>
                                 <p>
                                     Biji yang sudah dibersihkan , kemudian dijemur selama 3-5 hari tergantung dari kondisi cuaca.
                                     Untuk penyimpanan biji sebaiknya menggunakan karung plastik dengan kadar 10-12%.
                                     Hal ini dimaksudkan supaya dapat bertahan lama dan tidak mudah diserang oleh hama dan penyakit.
                                     <br>
                                     Biji yang mau dijadikan benih sebaiknya kadar airnya berkisar 9-10% dan disimpan dalam wadah yang
                                     tertutup seperti jergen atau drum untuk benih jumlah yang terbatas.
                                     Sedangkan benih yang jumlahnya banyak sebaiknya dikemas menggunakan plastik dengan ketebalan 0,2 mm
                                     kemudian dimasukkan ke dalam karung.
                                 </p>
                             </ol>
                             </p>

                         </ol>


                     </ol>
